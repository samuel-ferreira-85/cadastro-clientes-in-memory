    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int linhaSelecionada = tabelaClientes.getSelectedRow();
        
        if(linhaSelecionada >= 0) {
            
            String nome = txtNome.getText();
            String cpf = txtCpf.getText();
            String telefone = txtTelefone.getText();
            String endereco = txtEndereco.getText();
            String numero = txtNumero.getText();
            String cidade = txtCidade.getText();
            String estado = txtEstado.getText();
            
            Long cpfL = Long.parseLong(cpf);
            if (!isCamposValidos(nome, cpf, telefone, endereco, numero, cidade, estado)) {
                JOptionPane.showMessageDialog(this, "Há campos a serem preenchidos", "Atenção!", JOptionPane.WARNING_MESSAGE);
                return;
            } 
            Cliente newCliente = new Cliente(nome, cpf, telefone, endereco, numero, cidade, estado);
            Boolean isCadastrado = clienteDAO.alterar(cpfL, newCliente);
            if (isCadastrado) {
                modelo.removeRow(linhaSelecionada);
                modelo.addRow(new Object[]{newCliente.getNome(), newCliente.getCpf(), newCliente.getTelefone(), newCliente.getEndereco(), 
                    newCliente.getNumero(), newCliente.getCidade(), newCliente.getEstado()});
                JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso!", "Atualização",
                    JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
            }        
        
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum cliente selecionado.", "Atenção",
                    JOptionPane.WARNING_MESSAGE);
        }             
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

